void main() {
	int a = 7, b = 3, c = 0;
	c = a + b; //c=10
	c = a - b; //c=4
	c = a * b; //c=21
	c = a / b; //c=2
	c = a % b; //c=1

	c = 7 / 3; // 2
	c = 7 % 3; // 1
}

